package com.corejava.first.test.operators;

public class AssignmentOperatorsEx {

	public static void main(String[] args) {
		  int c = 10;
		   int a=2;
		   
		   System.out.println("c+=a "+(c+=a));
		   System.out.println("c-=a "+(c-=a));
		   System.out.println("c*=a "+(c*=a));
		   System.out.println("c/=a "+(c/=a));
		  
		   System.out.println("c%=a "+(c%=a));

	}

}
