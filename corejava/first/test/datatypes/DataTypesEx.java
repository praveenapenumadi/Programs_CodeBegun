package com.corejava.first.test.datatypes;

public class DataTypesEx {

	public static void main(String[] args) {
		
		     byte b = 5;
		     short s = 10;
		     int a = 20;
		     float f = 30f;
		     double d = 23.12;
		     long l = 9390558959l;
		     boolean flag = true;
		     char c = 'p';
		     System.out.println(b+" is a byte value");
		     System.out.println(s+" is a short value");
		     System.out.println(a+" is a Integer value");
		     System.out.println(f+" is a float value");
		     System.out.println(d+" is a double value");
		     System.out.println(l+" is a Long value");
		     System.out.println(flag+" is a boolean value");
		     System.out.println(c+" is a character");

	}

}
